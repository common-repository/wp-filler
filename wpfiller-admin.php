<h2>WP Filler</h2><i>by <a href="http://www.danrice.co.uk">Dan Rice</a></i>
<p>This plugin lets you insert filler text into any page or post. 
To use, simply insert the shortcode (like the example below), where paragraphs = the number of filler text paragraphs you want to include.</p>
<p><strong>[wp-filler paragraphs="1"]</strong></p>
